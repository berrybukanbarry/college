#TIPE DATA

#string
a ="Hello"
print(type(a))

#int
b = 20
print(type(b))

#float
c = 10.343
print(type(c))

#complex
d = 1j
print(type(d))

#list
e = {"a","b","c"}
print(type(e))

#tuple
f = ("a","b","c")
print(type(f))

#range
g = range(5)
print(type(g))

#dict
h = {"nama" : "ani", "umur" : 90}
print(type(h))

#set
i = {"ani","budi","tono"}
print(type(i))

#frozenset
j = frozenset({"ani","endang","hendra gozali"})
print(type(j))

#bool
k = True
print(type(k))

#bytes
l = b"hello"
print(type(l))

#byte array
m = bytearray (5)
print(type(m))

#memoryview
n = memoryview(bytes(5))
print(type(n))














