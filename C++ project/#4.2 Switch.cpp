#include <iostream>

using namespace std;
	
int main() {
	char nilai;
	
	cout<<"Masukkan nilai anda ('A - E'): ";
	cin>>nilai;
	
	switch (nilai){
		case 'A' :
			cout<<"Anda GG"<<endl;
			break;
		case 'B' :
			cout<<"Anda Tidak GG"<<endl;
			break;
		case 'C' :
			cout<<"Mehhh"<<endl;
			break;
		case 'D' :
			cout<<"CUPUHHHHHH"<<endl;
			break;
		case 'E' :
			cout<<"Dahlah cupuhhhhh!!"<<endl;
			break;
			default:
				cout<<"Nilai yang ada masukkan tidak adaaaaaaaaaa!!!!!!!"<<endl;
	}
	
	
}
